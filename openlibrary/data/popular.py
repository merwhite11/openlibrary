
def generate_popular_list(web, list_keys=None):
    """Generates a list of [ocaid, ol edition key] tuples from 
    popular lists

    param:
        lists - an ordered (prioritized) list of seeds to pick from

    1) Got a list by ol key
    2) Iterate over the list.seeds
    3) For each seed, get the document (i.e. in our case, an
    Edition) via web.ctx.site.get(seed) [expensive]. This is
    required in order to get editions `ocaid`.
    4) Append each edution to the popular list as a [ocaid, ol edition key] tuple,
    assuming it is not edition.get_ebook_status()['daisy_only']
    5) The list of [ocaid, ol edition key] tuples are then added to `popular.popular`
    """
    popular_list = []
    list_keys = list_keys or [
        '/people/mekBot/lists/OL104041L', 
        '/people/openlibrary/lists/OL104411L'
    ]
    seen = set()  # a list of already added books
    seeds = sum([web.ctx.site.get(list_key).seeds 
                 for list_key in list_keys], [])
    for seed in seeds:
        ed = web.ctx.site.get(seed.key)
        if ed.ocaid and not ed.get_ebook_info()['daisy_only']:
            if ed.ocaid not in seen:
                popular_list.append([ed.ocaid, ed.key])
                seen.add(ed.ocaid)
    return popular_list

